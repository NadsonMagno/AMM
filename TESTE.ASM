ORG 0 


INICIO:
	MOV	BL, C0
	
	


LOOP:
	IN 0
	CMP	AL, 08
	JZ	APAGA
	CMP	AL, 7F
	JZ	LIMPA
	CMP	AL, 0D
	JZ	ENTER
	MOV	[BL], AL
	INC	BL
	JMP	LOOP

APAGA:
	DEC	BL
	CMP	BL, C0
	JS	INICIO
	
	MOV	CL, 20
	MOV	[BL], CL
	JMP	LOOP
LIMPA:
	MOV	CL, 20
	MOV	[BL], CL
	DEC	BL
	CMP	BL, C0
	JS	INICIO
	JMP	LIMPA
ENTER:
	PUSH	BL
	POP	DL
	DIV	DL, C0
	
	MOV	CL, 10
	SUB	CL, DL
	LOOP2:
	MOV	DL, 20
	MOV	[BL], DL
	INC	BL
	DEC	CL
	JNZ	LOOP2
	MOV	CL,0
	JMP	LOOP
FIM:
    END